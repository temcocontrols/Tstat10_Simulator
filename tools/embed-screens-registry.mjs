#!/usr/bin/env node
/**
 * Reads screens-registry.json and writes screens-registry.embedded.mjs so
 * screen-paths.js can import registry data without JSON import attributes
 * (ESLint + broad browser compatibility).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { verifyScreensRegistry } from './read-screens-registry.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const src = path.join(root, 'screens-registry.json');
const out = path.join(root, 'screens-registry.embedded.mjs');

const data = JSON.parse(fs.readFileSync(src, 'utf8'));
verifyScreensRegistry(data);

const banner = `// Generated by tools/embed-screens-registry.mjs from screens-registry.json — do not edit.\n`;
const body = `export default ${JSON.stringify(data, null, 2)};\n`;
fs.writeFileSync(out, banner + body, 'utf8');
console.log(`[embed-screens-registry] Wrote ${path.relative(root, out)}`);
